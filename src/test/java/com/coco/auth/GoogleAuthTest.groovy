package com.coco.auth

import com.coco.auth.OAuth.GoogleAuth
import spock.lang.Specification

class GoogleAuthTest extends Specification {
    def "숫자1과 숫자2를 받아서 더한다"() {
        given:
        GoogleAuth auth = new GoogleAuth()

        when:
        def add = auth.add(num1, num2)

        then:
        add == result

        where:
        num1 | num2 | result
        10   | 20   | 30
        10   | 30   | 40
        30   | 20   | 50

    }
}
